import { useState } from 'react';
import './App.css'; // Cleared but imported for compatibility
import { Dashboard } from './components/Dashboard';
import { AgentManager } from './components/AgentManager';
import { Feedback } from './components/Feedback';

function App() {
  const [activePage, setActivePage] = useState('dashboard');

  const renderPage = () => {
    switch(activePage) {
      case 'dashboard': return <Dashboard />;
      case 'agents': return <AgentManager />;
      case 'feedback': return <Feedback />;
      default: return <Dashboard />;
    }
  };

  return (
    <div className="app-container">
      <nav className="navbar">
        <div className="brand">SCARMONIT /// NANO</div>
        <div className="nav-links">
          <button 
            className={`nav-item ${activePage === 'dashboard' ? 'active' : ''}`}
            onClick={() => setActivePage('dashboard')}
          >
            Mission Control
          </button>
          <button 
            className={`nav-item ${activePage === 'agents' ? 'active' : ''}`}
            onClick={() => setActivePage('agents')}
          >
            Neural Agents
          </button>
          <button 
            className={`nav-item ${activePage === 'artifacts' ? 'active' : ''}`}
            onClick={() => setActivePage('artifacts')}
          >
            Artifacts
          </button>
          <button 
            className={`nav-item ${activePage === 'feedback' ? 'active' : ''}`}
            onClick={() => setActivePage('feedback')}
          >
            Feedback
          </button>
        </div>
      </nav>

      <main className="main-content">
        {renderPage()}
      </main>

      <footer className="footer">
        <p>Powered by Gemini Nano Bana 3.0 Architecture â€¢ v4.2.0-alpha</p>
      </footer>
    </div>
  );
}

export default App;